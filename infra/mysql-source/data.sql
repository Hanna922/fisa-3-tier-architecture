/* =====================================================
   특정 라이프스테이지가 유독 많이 쓰는 업종
   - 전체 평균 대비 소비 비율
   ===================================================== */
SELECT
  CATEGORY,
  AVG(CASE WHEN LIFE_STAGE = 'NEW_JOB' THEN AMT END) AS TARGET_AVG,
  AVG(AMT) AS TOTAL_AVG,
  ROUND(
    AVG(CASE WHEN LIFE_STAGE = 'NEW_JOB' THEN AMT END)/ AVG(AMT), 2) AS RELATIVE_RATIO
FROM (
  SELECT LIFE_STAGE, 'INTERIOR' AS CATEGORY, INTERIOR_AM AS AMT FROM card_transaction
  UNION ALL SELECT LIFE_STAGE, 'INSUHOS', INSUHOS_AM FROM card_transaction
  UNION ALL SELECT LIFE_STAGE, 'TRVL', TRVL_AM FROM card_transaction
  UNION ALL SELECT LIFE_STAGE, 'CULTURE', CULTURE_AM FROM card_transaction
  UNION ALL SELECT LIFE_STAGE, 'GROCERY', GROCERY_AM FROM card_transaction
  UNION ALL SELECT LIFE_STAGE, 'HOSPITAL', HOS_AM FROM card_transaction
  UNION ALL SELECT LIFE_STAGE, 'RESTAURANT', RESTRNT_AM FROM card_transaction
  UNION ALL SELECT LIFE_STAGE, 'FUEL', FUEL_AM FROM card_transaction
) t
GROUP BY CATEGORY
HAVING RELATIVE_RATIO >= 1.3
ORDER BY RELATIVE_RATIO;

/* =====================================================
   라이프스테이지별 필수 vs 선택 소비 구조
   - 필수 : 병원(HOST) 마트(GROCERY) 교통(FUEL)
   - 선택 : 외식(RESTRNT) 여행(TRVL) 문화(CULTURE)
   ===================================================== */
