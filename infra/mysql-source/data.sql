  LIFE_STAGE,
  SUM(HOS_AM + GROCERY_AM + FUEL_AM)       AS ESSENTIAL_AMT,
  SUM(RESTRNT_AM + TRVL_AM + CULTURE_AM)   AS OPTIONAL_AMT
FROM card_transaction
GROUP BY LIFE_STAGE
ORDER BY LIFE_STAGE;

