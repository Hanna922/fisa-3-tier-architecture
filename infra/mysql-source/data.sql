-- 전체 라이프 스테이지 별
SELECT *
FROM (
  SELECT
    LIFE_STAGE,
    CATEGORY,
    SUM(AMT) AS TOTAL_AMT,
    RANK() OVER (
      PARTITION BY LIFE_STAGE
      ORDER BY SUM(AMT) DESC
    ) AS RNK
  FROM (
    SELECT LIFE_STAGE, 'INTERIOR'   AS CATEGORY, IFNULL(INTERIOR_AM,0)   AS AMT FROM card_transaction
    UNION ALL SELECT LIFE_STAGE, 'INSUHOS',   IFNULL(INSUHOS_AM,0)   FROM card_transaction
    UNION ALL SELECT LIFE_STAGE, 'TRVL',      IFNULL(TRVL_AM,0)      FROM card_transaction
    UNION ALL SELECT LIFE_STAGE, 'CULTURE',   IFNULL(CULTURE_AM,0)   FROM card_transaction
    UNION ALL SELECT LIFE_STAGE, 'GROCERY',   IFNULL(GROCERY_AM,0)   FROM card_transaction
    UNION ALL SELECT LIFE_STAGE, 'HOSPITAL',  IFNULL(HOS_AM,0)       FROM card_transaction
    UNION ALL SELECT LIFE_STAGE, 'RESTAURANT',IFNULL(RESTRNT_AM,0)   FROM card_transaction
    UNION ALL SELECT LIFE_STAGE, 'FUEL',      IFNULL(FUEL_AM,0)      FROM card_transaction
  ) t
  GROUP BY LIFE_STAGE, CATEGORY
) r
WHERE RNK <= 5
ORDER BY LIFE_STAGE, RNK;

